using Microsoft.AspNetCore.Mvc;

namespace Music_API.Controllers
{
    [ApiController]
    [Route("[controller]")]
    public class EXAMPLE_WeatherForecastController : ControllerBase
    {
        private static readonly string[] Summaries = new[]
        {
        "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
    };

        private readonly ILogger<EXAMPLE_WeatherForecastController> _logger;

        public EXAMPLE_WeatherForecastController(ILogger<EXAMPLE_WeatherForecastController> logger)
        {
            _logger = logger;
        }

        [HttpGet(Name = "GetWeatherForecast")]
        public IEnumerable<WeatherForecast> Get()   // there was a WeatherForecast.cs class auto generated like this one too but I killed him
        {
            return Enumerable.Range(1, 5).Select(index => new WeatherForecast
            {
                Date = DateTime.Now.AddDays(index),
                TemperatureC = Random.Shared.Next(-20, 55),
                Summary = Summaries[Random.Shared.Next(Summaries.Length)]
            })
            .ToArray();
        }
    }
}